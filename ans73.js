// This function shows how to assign and update variable values
function updateVariable() {
    var number = 69; // Initially assigns the value 69
    console.log("Initial value:", number); // Logs the initial value
    number = 138; // Updates the value to 138
    console.log("Updated value:", number); // Logs the updated value
}
updateVariable();
// We assign a value to a variable and then 
//update it, showing how values can change.
